BudgetTransaction _transaction(
  String id,
  DateTime date,
  int amount,
  String categoryId,
  String merchant, {
  String accountId = 'card-main',
  TransactionType type = TransactionType.expense,
  String? subcategoryId,
  String? comment,
  bool isLargePurchase = false,
  bool isRecurring = false,
  bool isConfirmed = true,
  bool isPotentialDuplicate = false,
  double classificationConfidence = 1,
  String? originalCategoryId,
  String? normalizedMerchant,
}) {
  return BudgetTransaction(
    id: id,
    userId: 'demo-user',
    accountId: accountId,
    date: date,
    amount: amount,
    currency: 'RUB',
    type: type,
    categoryId: categoryId,
    subcategoryId: subcategoryId,
    merchant: merchant,
    title: merchant,
    comment: comment,
    isLargePurchase: isLargePurchase,
    isRecurring: isRecurring,
    isConfirmed: isConfirmed,
    isPotentialDuplicate: isPotentialDuplicate,
    classificationConfidence: classificationConfidence,
    originalCategoryId: originalCategoryId,
    normalizedMerchant: normalizedMerchant,
  );
}

List<BudgetTransaction> _historyForMonth({
  required int month,
  required List<int> totals,
  required int income,
}) {
  const categoryIds = ['groceries', 'transport', 'cafes', 'fun'];
  const merchants = [
    ['Пятёрочка', 'Перекрёсток', 'ВкусВилл'],
    ['Московский транспорт', 'Яндекс Go', 'РЖД'],
    ['Surf Coffee', 'Теремок', 'Шоколадница'],
    ['Кинотеатр', 'Steam', 'Концерт'],
  ];
  final result = <BudgetTransaction>[];
  for (
    var categoryIndex = 0;
    categoryIndex < categoryIds.length;
    categoryIndex++
  ) {
    final total = totals[categoryIndex];
    final first = (total * 0.28).round();
    final second = (total * 0.33).round();
    final third = total - first - second;
    final amounts = [first, second, third];
    for (var index = 0; index < 3; index++) {
      result.add(
        _transaction(
          'history-$month-$categoryIndex-$index',
          DateTime(2026, month, 3 + categoryIndex * 5 + index * 2),
          amounts[index],
          categoryIds[categoryIndex],
          merchants[categoryIndex][index],
          accountId: index == 2 && categoryIndex.isEven ? 'cash' : 'card-main',
          normalizedMerchant: merchants[categoryIndex][index],
          isLargePurchase: amounts[index] >= 9000,
        ),
      );
    }
  }
  result.addAll([
    BudgetTransaction(
      id: 'history-$month-income',
      userId: 'demo-user',
      accountId: 'card-main',
      date: DateTime(2026, month, 1, 9),
      amount: income,
      currency: 'RUB',
      type: TransactionType.income,
      title: month.isEven ? 'Зарплата и премия' : 'Зарплата',
      merchant: 'Работодатель',
      normalizedMerchant: 'Работодатель',
      isRecurring: true,
    ),
    _transaction(
      'history-$month-subscription',
      DateTime(2026, month, 6, 11),
      499 + (month >= 5 ? 100 : 0),
      'subscriptions',
      'Яндекс Плюс',
      isRecurring: true,
      normalizedMerchant: 'Яндекс Плюс',
    ),
    _transaction(
      'history-$month-internet',
      DateTime(2026, month, 12, 10),
      650,
      'internet',
      'Домашний интернет',
      isRecurring: true,
      normalizedMerchant: 'Домашний интернет',
    ),
    BudgetTransaction(
      id: 'history-$month-savings',
      userId: 'demo-user',
      accountId: 'card-main',
      date: DateTime(2026, month, 2),
      amount: 8000 + month * 500,
      currency: 'RUB',
      type: TransactionType.savingsTransfer,
      title: 'В накопления',
    ),
    BudgetTransaction(
      id: 'history-$month-transfer',
      userId: 'demo-user',
      accountId: 'card-main',
      date: DateTime(2026, month, 9),
      amount: 12000,
      currency: 'RUB',
      type: TransactionType.transfer,
      title: 'Между своими счетами',
    ),
  ]);
  return result;
}

